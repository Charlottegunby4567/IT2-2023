# Lag et program som sjekker om input fra brukeren inneholder en gyldig RGB-farge. 
# RBG (Red, Green, Blue) er en måte å representere farger på som består av tre heltall fra og med 0 til og med 255, 
# som angir hvor mye av de tre fargene som skal være med i den endelige fargen.

# Noen eksempler:
	#• Svart: 0 0 0
	#• Hvit: 255 255 255
	#• Rød: 255 0 0
	#• Gul: 255 255 0


	#1. Lag et program som ber brukeren om tre tall som én input. Tallene skal skilles med mellomrom. Bruk split()-metoden for å dele brukerens input i tre separate tall. Lagre resultatet i tre variabler. 
	#2. Sjekk om variabelene inneholder tall. Hvis ikke, skriv ut en feilmelding.
		#○ Hint:  tekst.isnumeric() kan brukes for å sjekke om en string bare inneholder tall.
	#3. Konverter de tre string-tallene til heltall. Sjekk om de tre tallene er gyldige, dvs. fra mellom 0 og 256. Hvis ikke, skriv ut en feilmelding som forklarer hva som gikk galt. Skriv også en melding hvis alt gikk bra.


